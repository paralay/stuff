# Demander le nom de l'utilisateur
nom_utilisateur = input("Quel est votre nom? ")

# Demander l'âge de l'utilisateur
age_utilisateur = input("Quel est votre âge? ")

# Convertir l'âge en entier
age_utilisateur = int(age_utilisateur)

# Afficher un message personnalisé
print(f"Bonjour {nom_utilisateur}! Vous avez {age_utilisateur} ans.")